# Sumamos en python!
num1 = input("Ingresa el primer numero: ")

num2 = input("Ingresa el segundo numero: ")

# Convertir las entradas a enteros (int) o flotantes (float) si son decimales
num1 = float(num1)
num2 = float(num2)

resultado = num1 + num2

print("La suma de", {num1}," y ",{num2}, "es: ",{resultado})